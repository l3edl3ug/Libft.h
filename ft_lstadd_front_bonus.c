#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (lst == NULL || new == NULL)
		return ;
	new->next = *lst;
	*lst = new;
}
/*
ฟังก์ชัน ft_lstadd_front มีหน้าที่ในการเพิ่ม node ใหม่ 
(new) เข้าไปที่จุดเริ่มต้นของ linked list ที่มีอยู่แล้ว (*lst) 
โดยทำให้ node ใหม่กลายเป็น node แรกของลิสต์.
บรรทัด new->next = *lst;:
ฟังก์ชันจะทำให้ next ของ node ใหม่ (new) ชี้ไปที่ node แรกของลิสต์เดิม (*lst).
ตัวอย่าง: ถ้าลิสต์เดิมมี node เริ่มต้นเป็น A, และ node ใหม่เป็น B,
ฟังก์ชันนี้จะทำให้ B->next ชี้ไปที่ A.
ฟังก์ชันนี้จะเพิ่ม node ใหม่เข้าไปที่จุดเริ่มต้นของ linked list ที่มีอยู่แล้ว
โดยฟังก์ชันจะทำการเปลี่ยน pointer next
ของ node ใหม่ให้ชี้ไปที่ node แรกของลิสต์เดิม
จากนั้นจะเปลี่ยน pointer ของ node แรกของลิสต์ให้ชี้ไปที่ node ใหม่
ซึ่งทำให้ node ใหม่กลายเป็น node แรกของลิสต์
ตัวอย่างเช่น
สมมติว่ามีลิสต์ดังนี้:
A -> B -> C
และต้องการเพิ่ม node ใหม่ X ไปที่จุดเริ่มต้นของลิสต์.
หลังจากเรียกใช้ ft_lstadd_front(&lst, new) ลิสต์จะกลายเป็น:
X -> A -> B -> C
โดยที่ X->next ชี้ไปที่ A, และ *lst จะชี้ไปที่ X.
*/
